func FuzzParsePubspecLock(f *testing.F) {
	f.Add([]byte("# Generated by pub\npackages:\n  http:\n    dependency: \"direct main\"\n    version: \"1.2.1\"\n"))
	f.Add([]byte(""))
	f.Add([]byte("# comment only"))
	f.Add([]byte("packages:\n"))
	f.Add([]byte("sdks:\n  dart: \">=3.0.0 <4.0.0\"\n"))

	f.Fuzz(func(t *testing.T, data []byte) {
		defer func() { recover() }() //nolint:errcheck

		dir := t.TempDir()
		if err := os.WriteFile(filepath.Join(dir, "pubspec.lock"), data, 0o600); err != nil {
			return
		}
		loadPubspecLock(dir) //nolint:errcheck
	})
}
