func TestDetectCapabilities(t *testing.T) {
	dir := t.TempDir()
	src := `import 'dart:io';
import 'package:http/http.dart';

void main() {
  // Read a file
  final f = File('config.txt');
  // Run a process
  Process.run('ls', ['-la']);
}
`
	if err := os.WriteFile(filepath.Join(dir, "main.dart"), []byte(src), 0o600); err != nil {
		t.Fatal(err)
	}

	caps := Detect(dir)

	wantCaps := []string{"fs:read", "fs:write", "exec", "network"}
	for _, want := range wantCaps {
		if !caps.Has(want) {
			t.Errorf("expected capability %q to be detected", want)
		}
	}
}

func TestDetectNoCapabilities(t *testing.T) {
	dir := t.TempDir()
	src := `/// A simple utility library with no I/O.
int add(int a, int b) => a + b;

String greet(String name) => 'Hello, $name!';
`
	if err := os.WriteFile(filepath.Join(dir, "utils.dart"), []byte(src), 0o600); err != nil {
		t.Fatal(err)
	}

	caps := Detect(dir)
	if !caps.IsEmpty() {
		t.Errorf("expected no capabilities for benign code, got: %v", caps.List())
	}
}

func TestDetectDartImportVariants(t *testing.T) {
	dir := t.TempDir()
	src := `import "dart:mirrors";
import 'dart:ffi';
import "package:ffi/ffi.dart";
`
	if err := os.WriteFile(filepath.Join(dir, "reflection.dart"), []byte(src), 0o600); err != nil {
		t.Fatal(err)
	}

	caps := Detect(dir)
	if !caps.Has("reflect") {
		t.Error("expected 'reflect' capability from dart:mirrors import")
	}
	if !caps.Has("unsafe") {
		t.Error("expected 'unsafe' capability from dart:ffi / ffi imports")
	}
}

