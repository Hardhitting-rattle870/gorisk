func TestAdapterLoad(t *testing.T) {
	dir := t.TempDir()

	lockContent := `# Generated by pub
packages:
  http:
    dependency: "direct main"
    description:
      name: http
    source: hosted
    version: "1.2.1"
  async:
    dependency: transitive
    description:
      name: async
    source: hosted
    version: "2.11.0"
sdks:
  dart: ">=3.0.0 <4.0.0"
`
	if err := os.WriteFile(filepath.Join(dir, "pubspec.lock"), []byte(lockContent), 0o600); err != nil {
		t.Fatal(err)
	}

	a := Adapter{}
	g, err := a.Load(dir)
	if err != nil {
		t.Fatalf("Load: %v", err)
	}
	if g == nil {
		t.Fatal("Load returned nil graph")
	}

	if len(g.Modules) < 2 {
		t.Errorf("expected at least 2 modules, got %d", len(g.Modules))
	}

	if _, ok := g.Packages["http"]; !ok {
		t.Error("expected 'http' package in graph")
	}
	if _, ok := g.Packages["async"]; !ok {
		t.Error("expected 'async' package in graph")
	}

	// http is direct, so root should have an edge to it.
	rootName := filepath.Base(dir)
	rootEdges := g.Edges[rootName]
	foundHTTP := false
	for _, e := range rootEdges {
		if e == "http" {
			foundHTTP = true
		}
	}
	if !foundHTTP {
		t.Error("root should have edge to direct dep 'http'")
	}
}

