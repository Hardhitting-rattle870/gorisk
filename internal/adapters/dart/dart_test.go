func TestLoadPubspecLock(t *testing.T) {
	dir := t.TempDir()

	lockContent := `# Generated by pub
packages:
  characters:
    dependency: transitive
    description:
      name: characters
      sha256: "04fbf07a0853e911c4f60b145db25e24f4e87571"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "1.3.0"
  http:
    dependency: "direct main"
    description:
      name: http
      sha256: "a2bbf9d017fcefaa86a23c9f7f4ad0f561bc4f12"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "1.2.1"
  collection:
    dependency: "direct dev"
    description:
      name: collection
    source: hosted
    version: "1.18.0"
  async:
    dependency: transitive
    description:
      name: async
    source: hosted
    version: "2.11.0"
sdks:
  dart: ">=3.0.0 <4.0.0"
`
	if err := os.WriteFile(filepath.Join(dir, "pubspec.lock"), []byte(lockContent), 0o600); err != nil {
		t.Fatal(err)
	}

	pkgs, err := loadPubspecLock(dir)
	if err != nil {
		t.Fatalf("loadPubspecLock: %v", err)
	}

	byName := make(map[string]DartPackage)
	for _, p := range pkgs {
		byName[p.Name] = p
	}

	// http is a direct main dep.
	if _, ok := byName["http"]; !ok {
		t.Error("expected 'http' in packages")
	}
	if byName["http"].Version != "1.2.1" {
		t.Errorf("http version: got %q, want %q", byName["http"].Version, "1.2.1")
	}
	if !byName["http"].Direct {
		t.Error("http should be a direct dep")
	}

	// collection is a direct dev dep.
	if _, ok := byName["collection"]; !ok {
		t.Error("expected 'collection' in packages")
	}
	if !byName["collection"].Direct {
		t.Error("collection should be a direct dep")
	}

	// characters is transitive.
	if _, ok := byName["characters"]; !ok {
		t.Error("expected 'characters' in packages")
	}
	if byName["characters"].Direct {
		t.Error("characters should not be a direct dep")
	}
	if byName["characters"].Version != "1.3.0" {
		t.Errorf("characters version: got %q, want %q", byName["characters"].Version, "1.3.0")
	}

	// async is transitive.
	if byName["async"].Direct {
		t.Error("async should not be a direct dep")
	}
}

func TestLoadPubspecYAML(t *testing.T) {
	dir := t.TempDir()

	content := `name: my_app
description: A Flutter application.
version: 1.0.0

environment:
  sdk: ">=3.0.0 <4.0.0"

dependencies:
  flutter:
    sdk: flutter
  http: ^1.2.1
  dio: ^5.0.0
  path_provider: "^2.1.0"

dev_dependencies:
  flutter_test:
    sdk: flutter
  flutter_lints: ^3.0.0
`
	if err := os.WriteFile(filepath.Join(dir, "pubspec.yaml"), []byte(content), 0o600); err != nil {
		t.Fatal(err)
	}

	pkgs, err := loadPubspecYAML(dir)
	if err != nil {
		t.Fatalf("loadPubspecYAML: %v", err)
	}

	byName := make(map[string]DartPackage)
	for _, p := range pkgs {
		byName[p.Name] = p
	}

	if _, ok := byName["http"]; !ok {
		t.Error("expected 'http' in packages")
	}
	if _, ok := byName["dio"]; !ok {
		t.Error("expected 'dio' in packages")
	}
	if _, ok := byName["path_provider"]; !ok {
		t.Error("expected 'path_provider' in packages")
	}
	if _, ok := byName["flutter_lints"]; !ok {
		t.Error("expected 'flutter_lints' in packages")
	}
	// All should be direct.
	for _, p := range pkgs {
		if !p.Direct {
			t.Errorf("package %q should be Direct=true in pubspec.yaml parse", p.Name)
		}
	}
	// sdk entries like "flutter" should be skipped.
	if _, ok := byName["sdk"]; ok {
		t.Error("sdk key should not appear as a package")
	}
}

